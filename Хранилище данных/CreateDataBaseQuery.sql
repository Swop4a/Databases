CREATE PROCEDURE SW
AS
	-- STEP 1
	DECLARE @DateForPartFP varchar(8);
	DECLARE @DateForArch varchar(8);

	SET @DateForPartFP = CAST((SELECT TOP 1 [value] FROM sys.partition_range_values 
		WHERE function_id = (SELECT function_id FROM sys.partition_functions WHERE name = 'PFFastPurchases_Date')
		ORDER BY boundary_id DESC) AS varchar(8));

	SET @DateForArch = CAST((SELECT TOP 1 [value] FROM sys.partition_range_values 
		WHERE function_id = (SELECT function_id FROM sys.partition_functions WHERE name = 'PFFastPurchases_Date')
		ORDER BY boundary_id ASC) AS varchar(8));

	DECLARE @Date_1 DATE;
	SET @Date_1 = DATEADD(YEAR, 1, CAST(@DateForPartFP as DATE)); --2017
	DECLARE @Date_2 DATE;
	SET @Date_2 = DATEADD(YEAR, 1, CAST(@DateForArch as DATE));   --2014

	ALTER PARTITION SCHEME PSFastPurchases_Date NEXT USED [FrequentlyUsedFG];
	ALTER PARTITION SCHEME PSFastPurchasesArch NEXT USED [FrequentlyUsedFG];

	ALTER PARTITION FUNCTION PFFastPurchases_Date() SPLIT RANGE (CAST(CONVERT (varchar(8), @Date_1, 112) as bigint));
	ALTER PARTITION FUNCTION PFFastPurchasesArch() SPLIT RANGE (CAST(CONVERT (varchar(8), @Date_2, 112) as bigint));
	-- STEP 2
	DECLARE @last_section_number int;
	SET @last_section_number = (SELECT TOP 1 [partition_number] FROM sys.partitions WHERE OBJECT_ID = 
		(SELECT OBJECT_ID FROM Sys.tables WHERE name = 'FastPurchasesArch') ORDER BY [partition_number] DESC) - 1;

	ALTER TABLE FastPurchases SWITCH PARTITION 2 TO FastPurchasesArch PARTITION @last_section_number;
	-- STEP 3
	DECLARE @Date_First bigint;
	DECLARE @Date_FirstArch bigint;

	SET @Date_First = CAST((SELECT TOP 1 [value] FROM sys.partition_range_values 
		WHERE function_id = (SELECT function_id FROM sys.partition_functions WHERE name = 'PFFastPurchases_Date')
		ORDER BY boundary_id ASC) as bigint);

	ALTER PARTITION FUNCTION PFFastPurchases_Date() MERGE RANGE (@Date_First);
	-- STEP 4
	SET @DateForArch = CAST((SELECT TOP 1 [value] FROM sys.partition_range_values 
		WHERE function_id = (SELECT function_id FROM sys.partition_functions WHERE name = 'PFFastPurchasesArch')
		ORDER BY boundary_id DESC) AS varchar(8));
	SET @Date_2 = DATEADD(YEAR, -1, CAST(@DateForArch as DATE));
	PRINT CONVERT (varchar(8), @Date_2, 112);
	ALTER PARTITION FUNCTION PFFastPurchasesArch() MERGE RANGE (CAST(CONVERT (varchar(8), @Date_2, 112) as bigint));
	ALTER PARTITION SCHEME PSFastPurchasesArch NEXT USED [FastGrowingFG];
	ALTER PARTITION FUNCTION PFFastPurchasesArch() SPLIT RANGE (CAST(CONVERT (varchar(8), @Date_2, 112) as bigint));
	-- STEP 5
GO

